async function main() {
  const regions = await prisma.plateRegion.findMany();
  let updated = 0;
  for (const r of regions) {
    const correctKana = KANA_MAP[r.name];
    if (correctKana && r.kana !== correctKana) {
      await prisma.plateRegion.update({
        where: { id: r.id },
        data: { kana: correctKana },
      });
      console.log(`  ✅ ${r.name}: ${r.kana} → ${correctKana}`);
      updated++;
    }
  }
  console.log(`\n完了: ${updated}件の読み仮名を修正しました`);
}

main()
  .catch((e) => { console.error(e); process.exit(1); })
  .finally(() => prisma.$disconnect());
