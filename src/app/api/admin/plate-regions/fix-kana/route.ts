/**
 * POST /api/admin/plate-regions/fix-kana
 * 地名マスタの kana フィールドを正しいひらがなに一括修正
 */
export async function POST() {
  try {
    const regions = await prisma.plateRegion.findMany();
    let updated = 0;

    for (const r of regions) {
      const correctKana = KANA_MAP[r.name];
      if (correctKana && r.kana !== correctKana) {
        await prisma.plateRegion.update({
          where: { id: r.id },
          data: { kana: correctKana },
        });
        updated++;
      }
    }

    return NextResponse.json({ message: `${updated}件の読み仮名を修正しました`, updated });
  } catch (e) {
    console.error(e);
    return NextResponse.json({ error: "修正に失敗しました" }, { status: 500 });
  }
}
